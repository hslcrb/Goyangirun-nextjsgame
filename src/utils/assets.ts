export function drawPixelArt(ctx: CanvasRenderingContext2D, pixelData: string[], startX: number, startY: number, size: number, alpha: number = 1) {
  ctx.globalAlpha = Math.max(0, alpha);
  for (let y = 0; y < pixelData.length; y++) {
    const row = pixelData[y];
    for (let x = 0; x < row.length; x++) {
      const char = row[x];
      if (char !== ' ') {
        ctx.fillStyle = colors[char as keyof typeof colors] || '#000';
        ctx.fillRect(startX + x * size, startY + y * size, size, size);
      }
    }
  }
  ctx.globalAlpha = 1;
}

